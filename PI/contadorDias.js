function changeImageROT1() {
    var image = document.getElementById("image");
    if (image.src == "resources/logobranco.png") {
       image.src = "resources/testeRoteiro/ROT1.png";
    }
    else {
       image.src = "resources/testeRoteiro/ROT1.png";
    }
}

